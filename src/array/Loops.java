package array;

public class Loops {
    public static void main(String[] args) {

        for (int i =0; i< 10; i++) {
            System.out.println("=============");
            System.out.println("i= " + i);
            for (int j=0; j < 10; j++) {
                System.out.println("j = " + j);
            }

            System.out.println("+++++++");

        }

    }
}
